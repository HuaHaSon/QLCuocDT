        public static string MaKH;
        private void simpleButton1_Click(object sender, EventArgs e)
        {
                     
            if (txtmakh.Text == "")
            {
                MessageBox.Show("Chọn Đối tượng cần nhập");
            }
            else
            {
                if(g==null)
                {
                    f.MaKH = txtmakh.Text;
                    f.Change();
                    this.Close();
                }
                else if(f==null)
                {
                    g.MaKH = txtmakh.Text;
                    g.Change();
                    this.Close();
                }
                
            }
            
            
        }
